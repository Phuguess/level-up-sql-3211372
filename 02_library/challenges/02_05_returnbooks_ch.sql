-- Return three books to the library using
-- their bar codes.
-- Return date: July 5, 2022
-- Book 1: 6435968624
-- Book 2: 5677520613
-- Book 3: 8730298424

UPDATE LOANS
SET RETURNEDDATE = '2022-07-05'
WHERE BOOKID IN (
  SELECT BOOKID 
  FROM BOOKS 
  WHERE BARCODE IN (6435968624,5677520613,8730298424))

SELECT *
FROM LOANS
WHERE RETURNEDDATE='2022-07-05'